#FOR LOOP TESTING STUFF
#Loop1 - Counts 0-50 by increments of 1
for x in range(0, 51, 1):
    print(x)
#Loop2 - Counts 50-0 by increments of 1
for x in range(50, -1, -1):
    print(x)
#Loop3 - Counts 30-50 by increments of 1
for x in range(30, 51, 1):
    print(x)
#Loop4 - Counts 50-10 by increments of 2
for x in range(50, 9, -2):
    print(x)
#Loop5 - Counts 100-200 by increments of 5
for x in range(100, 201, 5):
    print(x)